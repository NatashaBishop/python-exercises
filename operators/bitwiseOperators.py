# bitwise operations manipulate the binary representations of the numbers
#    Bitwise NOT (~)
#    Bitwise right shift (>>)
#    Bitwise left shift (<<)
#    Bitwise AND (&)
#    Bitwise XOR (^)
#    Bitwise OR (|)
a = 10
b = 4
print(a & b)
print(a | b)
print(~a)
print(a ^ b)
print(a >> 2)
print(a << 2)
# Output
# 0
# 14
# -11
# 14
# 2
# 40



