# Create a list of numbers
numbers = [10, 20, 30, 40, 50]

# Print the item at index 0 (first position in the list)
print(numbers[0])    
# Output: 10

# Print the item at index 2 (third position â€” counting starts at 0)
print(numbers[2])    
# Output: 30

# Print the last item using negative indexing
# -1 always means "last element"
print(numbers[-1])   
# Output: 50

# Print the third item from the end
# -3 counts backward: -1 last, -2 second last, -3 third last
print(numbers[-3])   
# Output: 30
