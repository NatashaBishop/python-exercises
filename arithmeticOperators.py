# The precedence of Arithmetic Operators in Python is as follows:
#    P – Parentheses
#    E – Exponentiation
#    M – Multiplication (Multiplication and division have the same precedence)
#    D – Division
#    A – Addition (Addition and subtraction have the same precedence)
#    S – Subtraction

# example1
a = 9
b = 4
add = a + b
sub = a - b
mul = a * b
mod = a % b
p = a ** b
print(add)
print(sub)
print(mul)
print(mod)
print(p)

